package program;
public class SWITCH {

	public static void main(String[] args) {
for(int i=0;i<=3;i++)
{
	switch(i)
	{
	case 0:
	System.out.println("The value of i is:"+(i+1));
	break;
	
	case 1:
		System.out.println("The value of i is:"+(i+1));
		break;
		
	case 2:
		System.out.println("The value of i is:"+(i+1));
		break;
		
		default:
			System.out.println("The value of i is greater than 2");
	}
}
	}

}
